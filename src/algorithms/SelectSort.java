package algorithms;

public class SelectSort {

    public static void sort(int[] arr){
        int minIndex = 0;
        for(int i = 0;i<arr.length-1;i++){
            minIndex = i;
            for(int j=i+1;j<arr.length;j++){
                if (arr[minIndex]>arr[j]){
                    minIndex = j;
                }
            }
            int temp = arr[i];
            arr[i] = arr[minIndex];
            arr[minIndex] = temp;
        }
    }
}
